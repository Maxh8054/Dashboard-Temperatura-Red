// Parse date from DD/MM/YYYY format
export const parseDate = (dateStr: string): Date => {
  const parts = dateStr.split('/');
  return new Date(parseInt(parts[2]), parseInt(parts[1]) - 1, parseInt(parts[0]));
};

// Transform raw data to typed data
export const machineData: MachineData[] = rawData.map(row => ({
  machine: row[0],
  date: row[1],
  tempBelow84: parseNumber(row[2]),
  temp84to96: parseNumber(row[3]),
  temp97Above: parseNumber(row[4])
}));

// Get chart data with parsed dates
export const getChartData = () => {
  return machineData.map(d => ({
    ...d,
    parsedDate: parseDate(d.date),
    totalHours: d.tempBelow84 + d.temp84to96 + d.temp97Above
  })).sort((a, b) => a.parsedDate.getTime() - b.parsedDate.getTime());
};

// Calculate statistics
export const getStatistics = () => {
  const data = getChartData();
  const totalRecords = data.length;
  
  const avgBelow84 = data.reduce((sum, d) => sum + d.tempBelow84, 0) / totalRecords;
  const avg84to96 = data.reduce((sum, d) => sum + d.temp84to96, 0) / totalRecords;
  const avg97Above = data.reduce((sum, d) => sum + d.temp97Above, 0) / totalRecords;
  
  const totalBelow84 = data.reduce((sum, d) => sum + d.tempBelow84, 0);
  const total84to96 = data.reduce((sum, d) => sum + d.temp84to96, 0);
  const total97Above = data.reduce((sum, d) => sum + d.temp97Above, 0);
  
  const maxBelow84 = Math.max(...data.map(d => d.tempBelow84));
  const max84to96 = Math.max(...data.map(d => d.temp84to96));
  const max97Above = Math.max(...data.map(d => d.temp97Above));
  
  const daysWithHighTemp = data.filter(d => d.temp97Above > 0).length;
  
  return {
    totalRecords,
    avgBelow84,
    avg84to96,
    avg97Above,
    totalBelow84,
    total84to96,
    total97Above,
    maxBelow84,
    max84to96,
    max97Above,
    daysWithHighTemp,
    dateRange: {
      start: data[0]?.parsedDate,
      end: data[data.length - 1]?.parsedDate
    }
  };
};

// Get monthly aggregated data
export const getMonthlyData = () => {
  const data = getChartData();
  const monthlyMap = new Map<string, { 
    month: string; 
    tempBelow84: number; 
    temp84to96: number; 
    temp97Above: number; 
    count: number;
    year: number;
    monthNum: number;
  }>();
  
  data.forEach(d => {
    const monthKey = `${d.parsedDate.getFullYear()}-${String(d.parsedDate.getMonth() + 1).padStart(2, '0')}`;
    const existing = monthlyMap.get(monthKey) || { 
      month: monthKey, 
      tempBelow84: 0, 
      temp84to96: 0, 
      temp97Above: 0, 
      count: 0,
      year: d.parsedDate.getFullYear(),
      monthNum: d.parsedDate.getMonth() + 1
    };
    existing.tempBelow84 += d.tempBelow84;
    existing.temp84to96 += d.temp84to96;
    existing.temp97Above += d.temp97Above;
    existing.count += 1;
    monthlyMap.set(monthKey, existing);
  });
  
  return Array.from(monthlyMap.values())
    .sort((a, b) => a.month.localeCompare(b.month))
    .map(m => ({
      ...m,
      monthName: new Date(m.year, m.monthNum - 1).toLocaleDateString('pt-BR', { month: 'short', year: 'numeric' })
    }));
};

// Get yearly data
export const getYearlyData = () => {
  const data = getChartData();
  const yearlyMap = new Map<number, { 
    year: number; 
    tempBelow84: number; 
    temp84to96: number; 
    temp97Above: number; 
    count: number;
  }>();
  
  data.forEach(d => {
    const year = d.parsedDate.getFullYear();
    const existing = yearlyMap.get(year) || { year, tempBelow84: 0, temp84to96: 0, temp97Above: 0, count: 0 };
    existing.tempBelow84 += d.tempBelow84;
    existing.temp84to96 += d.temp84to96;
    existing.temp97Above += d.temp97Above;
    existing.count += 1;
    yearlyMap.set(year, existing);
  });
  
  return Array.from(yearlyMap.values()).sort((a, b) => a.year - b.year);
};
